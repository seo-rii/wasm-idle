import { createHash } from 'node:crypto';
import { cp, mkdir, readFile, readdir, rm, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'vite';

const THIS_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(THIS_DIR, '..');
const STATIC_DIR = path.join(REPO_ROOT, 'static');
const VERSION_MODULE_PATH = path.join(
	REPO_ROOT,
	'src/lib/playground/staticRuntimeModuleVersion.ts'
);
const packageJson = JSON.parse(await readFile(path.join(REPO_ROOT, 'package.json'), 'utf8'));

const modules = [
	{
		name: 'assemblyscript',
		entry: 'scripts/runtime-modules/assemblyscript.ts',
		packages: ['assemblyscript', '@assemblyscript/loader'],
		licenses: [['node_modules/assemblyscript/LICENSE', 'LICENSE.txt']]
	},
	{
		name: 'duckdb',
		entry: 'scripts/runtime-modules/duckdb.ts',
		packages: ['@duckdb/duckdb-wasm'],
		licenses: [['node_modules/@duckdb/duckdb-wasm/LICENSE', 'LICENSE.txt']]
	},
	{
		name: 'php',
		entry: 'scripts/runtime-modules/php.ts',
		packages: ['@php-wasm/web-8-4', '@php-wasm/universal'],
		licenses: [['node_modules/@php-wasm/web-8-4/LICENSE', 'LICENSE.txt']]
	},
	{
		name: 'ruby',
		entry: 'scripts/runtime-modules/ruby.ts',
		packages: ['@ruby/3.4-wasm-wasi', '@ruby/wasm-wasi'],
		licenses: []
	},
	{
		name: 'sqlite',
		entry: 'scripts/runtime-modules/sqlite.ts',
		packages: ['sql.js'],
		licenses: [['node_modules/sql.js/LICENSE', 'LICENSE.txt']]
	}
];

const packageVersion = (name) =>
	packageJson.devDependencies?.[name] || packageJson.dependencies?.[name] || 'unknown';

async function collectFiles(rootDir, currentDir = rootDir) {
	const files = [];
	for (const entry of await readdir(currentDir, { withFileTypes: true })) {
		const entryPath = path.join(currentDir, entry.name);
		if (entry.isDirectory()) {
			files.push(...(await collectFiles(rootDir, entryPath)));
		} else if (entry.isFile() && entry.name !== 'runtime-manifest.v1.json') {
			const bytes = await readFile(entryPath);
			files.push({
				path: path.relative(rootDir, entryPath).split(path.sep).join('/'),
				bytes: bytes.byteLength,
				sha256: createHash('sha256').update(bytes).digest('hex')
			});
		}
	}
	return files.sort((left, right) => left.path.localeCompare(right.path));
}

async function writeManifest(targetDir, packages, runtimeModule = 'runtime.mjs') {
	await writeFile(
		path.join(targetDir, 'runtime-manifest.v1.json'),
		`${JSON.stringify(
			{
				formatVersion: 1,
				runtimeModule,
				packages: Object.fromEntries(packages.map((name) => [name, packageVersion(name)])),
				files: await collectFiles(targetDir)
			},
			null,
			2
		)}\n`,
		'utf8'
	);
}

async function writeVersionModule(targetDirs) {
	const hash = createHash('sha256');
	for (const targetDir of [...targetDirs].sort()) {
		hash.update(path.relative(STATIC_DIR, targetDir));
		hash.update(await readFile(path.join(targetDir, 'runtime-manifest.v1.json')));
	}
	const version = hash.digest('hex').slice(0, 16);
	await writeFile(
		VERSION_MODULE_PATH,
		`// Generated by scripts/build-static-runtime-modules.mjs.\nexport const STATIC_RUNTIME_MODULE_VERSION = '${version}';\n`,
		'utf8'
	);
}

async function copyLicenses(targetDir, licenses) {
	for (const [source, target] of licenses) {
		const sourcePath = path.join(REPO_ROOT, source);
		if (!(await stat(sourcePath).catch(() => null))?.isFile()) continue;
		await cp(sourcePath, path.join(targetDir, target));
	}
}

async function buildRuntimeModule(config) {
	const targetDir = path.join(STATIC_DIR, `wasm-${config.name}`);
	await rm(targetDir, { recursive: true, force: true });
	await build({
		root: REPO_ROOT,
		configFile: false,
		publicDir: false,
		logLevel: 'warn',
		base: './',
		assetsInclude: ['**/*.wasm', '**/*.so', '**/*.dat'],
		build: {
			target: 'es2022',
			outDir: targetDir,
			emptyOutDir: true,
			assetsInlineLimit: 0,
			modulePreload: false,
			minify: 'esbuild',
			rollupOptions: {
				preserveEntrySignatures: 'strict',
				input: path.join(REPO_ROOT, config.entry),
				output: {
					format: 'es',
					entryFileNames: 'runtime.mjs',
					chunkFileNames: 'chunks/[name]-[hash].mjs',
					assetFileNames: 'assets/[name]-[hash][extname]'
				}
			}
		}
	});
	await copyLicenses(targetDir, config.licenses);
	await writeManifest(targetDir, config.packages);
	console.log(`Built static ${config.name} runtime module in ${path.relative(REPO_ROOT, targetDir)}`);
	return targetDir;
}

async function syncWasmerSdk() {
	const sourceDir = path.join(REPO_ROOT, 'node_modules', '@wasmer', 'sdk', 'dist');
	const targetDir = path.join(STATIC_DIR, 'wasm-bash', 'sdk');
	await rm(targetDir, { recursive: true, force: true });
	await mkdir(targetDir, { recursive: true });
	for (const fileName of ['index.mjs', 'worker.mjs', 'wasmer_js_bg.wasm']) {
		await cp(path.join(sourceDir, fileName), path.join(targetDir, fileName));
	}
	await cp(path.join(REPO_ROOT, 'node_modules/@wasmer/sdk/LICENSE'), path.join(targetDir, 'LICENSE.txt'));
	await writeManifest(targetDir, ['@wasmer/sdk'], 'index.mjs');
	console.log(`Synced static Wasmer SDK in ${path.relative(REPO_ROOT, targetDir)}`);
	return targetDir;
}

const targetDirs = [];
for (const config of modules) targetDirs.push(await buildRuntimeModule(config));
targetDirs.push(await syncWasmerSdk());
await writeVersionModule(targetDirs);
